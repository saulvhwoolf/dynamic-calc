function checkExeptions(poke, importOptions) {
	switch (poke) {
	case 'Aegislash':
		poke = "Aegislash-Shield";
		break;
	case 'Basculin-Blue-Striped':
		poke = "Basculin";
		break;
	case 'Gastrodon-East':
		poke = "Gastrodon";
		break;
	case 'Mimikyu-Busted-Totem':
		poke = "Mimikyu-Totem";
		break;
	case 'Mimikyu-Busted':
		poke = "Mimikyu";
		break;
	case 'Pikachu-Belle':
	case 'Pikachu-Cosplay':
	case 'Pikachu-Libre':
	case 'Pikachu-Original':
	case 'Pikachu-Partner':
	case 'Pikachu-PhD':
	case 'Pikachu-Pop-Star':
	case 'Pikachu-Rock-Star':
		if (!TITLE.includes("Imperium")) {
			poke = "Pikachu";
		}		
		break;
	case 'Vivillon-Fancy':
	case 'Vivillon-Pokeball':
		poke = "Vivillon";
		break;
	case 'Flabébé-White':
	case 'Flabébé-Blue':
	case 'Flabébé-Orange':
	case 'Flabébé-Yellow':
		poke = "Flabébé";
		break;
	case 'Floette-White':
	case 'Floette-Blue':
	case 'Floette-Orange':
	case 'Floette-Yellow':
		poke = "Floette";
		break;
	case 'Florges-White':
	case 'Florges-Blue':
	case 'Florges-Orange':
	case 'Florges-Yellow':
		poke = "Florges";
		break;
	}

	try {
		if (shouldApplyImportRomReplacements(importOptions) && poke != "" && backup_data["pok_replacements"] && backup_data["pok_replacements"][cleanString(poke)]) {
			poke = SPECIES_BY_ID[gen][backup_data["pok_replacements"][cleanString(poke)]].name
		}
	} catch {
		return poke
	}
	



	// try replacing spaces with dashes
	if (typeof pokedex[poke] == 'undefined' && pokedex[poke.replace(" ", "-")]) {
		poke = poke.replace(" ", "-")
		return poke
	}

	// try replacing dashes with spaces
	if (typeof pokedex[poke] == 'undefined' && pokedex[poke.replace("-", " ")]) {
		poke = poke.replace("-", " ")
		return poke
	}

	// try using only first part of name when separated by dashes
	if (typeof pokedex[poke] == 'undefined' && pokedex[poke.split("-")[0]]) {
		poke = poke.split("-")[0]
		return poke
	}


	return poke;

}

