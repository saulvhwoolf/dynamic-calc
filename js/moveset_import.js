function decodeDsPackedBoxPayload(payloadBytes) {
	var bytes = payloadBytes instanceof Uint8Array
		? payloadBytes
		: new Uint8Array(payloadBytes || new ArrayBuffer(0));
	if (bytes.length < 18) {
		throw new Error("Packed DS box payload is too short");
	}

	var magic = String.fromCharCode(bytes[0], bytes[1], bytes[2], bytes[3]);
	if (magic !== "DPB1" && magic !== "DPB2") {
		throw new Error("Unexpected DS packed box header: " + magic);
	}

	if (typeof loadPokeLuaGen4RawBoxDump !== "function") {
		throw new Error("DS Lua box dump importer is unavailable");
	}

	var trainerId = readUint16LE(bytes, 4);
	var secretId = readUint16LE(bytes, 6);
	var partyCount = bytes[8] >>> 0;
	var boxCount = bytes[9] >>> 0;
	var partyStructSize = readUint16LE(bytes, 10);
	var boxStructSize = readUint16LE(bytes, 12);
	var boxSlotsDumped = readUint16LE(bytes, 14);
	var currentBoxIndex = bytes[16] >>> 0;
	var headerSize = 18;
	var partyByteLength = partyCount * partyStructSize;
	var boxByteLength = boxSlotsDumped * boxStructSize;
	var expectedLength = headerSize + partyByteLength + boxByteLength;

	if (bytes.length < expectedLength) {
		throw new Error("Packed DS box payload is truncated");
	}

	var partyBytes = bytes.slice(headerSize, headerSize + partyByteLength);
	var boxBytes = bytes.slice(headerSize + partyByteLength, expectedLength);
	var dump = {
		trainerId: trainerId,
		secretId: secretId,
		partyCount: partyCount,
		currentBoxIndex: currentBoxIndex,
		boxCount: boxCount,
		partyStructSize: partyStructSize,
		boxStructSize: boxStructSize,
		boxSlotsDumped: boxSlotsDumped,
		partyEncoding: "hex",
		boxesEncoding: "hex",
		party: uint8ArrayToHexString(partyBytes),
		boxes: uint8ArrayToHexString(boxBytes),
	};

	var result = loadPokeLuaGen4RawBoxDump(dump);
	if (!result || typeof result.showdownImport !== "string") {
		throw new Error("DS packed box decode did not return showdown text");
	}

	var deadMons = [];
	if (magic === "DPB2") {
		var deadCountOffset = expectedLength;
		if (bytes.length < deadCountOffset + 2) {
			throw new Error("Packed DS dead trailer is truncated");
		}
		var deadCount = readUint16LE(bytes, deadCountOffset);
		var deadRecordOffset = deadCountOffset + 2;
		var requiredBytes = deadRecordOffset + (deadCount * 6);
		if (bytes.length < requiredBytes) {
			throw new Error("Packed DS dead records are truncated");
		}

		for (var deadIndex = 0; deadIndex < deadCount; deadIndex++) {
			var recordOffset = deadRecordOffset + (deadIndex * 6);
			var box = bytes[recordOffset] >>> 0;
			var slot = bytes[recordOffset + 1] >>> 0;
			var speciesId = readUint16LE(bytes, recordOffset + 2);
			var metLocationId = readUint16LE(bytes, recordOffset + 4);
			if (!speciesId) {
				continue;
			}

			deadMons.push({
				speciesName: resolveDsPackedSpeciesName(speciesId),
				speciesId: speciesId,
				nickname: "",
				met: resolveDsPackedMetLocationName(metLocationId),
				metLocationId: metLocationId,
				box: box,
				slot: slot,
				source: "desmume",
			});
		}
	}

	return {
		trainerId: trainerId,
		secretId: secretId,
		showdownImport: result.showdownImport,
		deadMons: deadMons,
		boxCount: boxCount,
		currentBoxIndex: currentBoxIndex,
	};
}

function decodeNullPackedBoxToShowdownText(payloadBytes) {
	var bytes = payloadBytes instanceof Uint8Array
		? payloadBytes
		: new Uint8Array(payloadBytes || new ArrayBuffer(0));
	if (bytes.length === 0) {
		return "";
	}

	var MON_BITS = 112;
	var bitStart = 0;
	var trainerId = null;
	var secretId = null;
	if (
		bytes.length >= 8 &&
		bytes[0] === 78 && // N
		bytes[1] === 66 && // B
		bytes[2] === 88 && // X
		bytes[3] === 49    // 1
	) {
		trainerId = (bytes[4] | (bytes[5] << 8)) >>> 0;
		secretId = (bytes[6] | (bytes[7] << 8)) >>> 0;
		bitStart = 64;
		try {
			if (typeof localStorage !== "undefined") {
				var combinedTrainerKey = (bytes[4] | (bytes[5] << 8) | (bytes[6] << 16) | (bytes[7] << 24)) >>> 0;
				localStorage.lastTid = String(combinedTrainerKey);
			}
		} catch (_err) {
		}
	}
	var totalBits = (bytes.length * 8) - bitStart;
	if (totalBits < 0) {
		totalBits = 0;
	}
	var monCount = Math.floor(totalBits / MON_BITS);

	var mons = Array.isArray(window.nullMons) ? window.nullMons : [];
	var moves = Array.isArray(window.nullMoves) ? window.nullMoves : [];
	var items = Array.isArray(window.nullItems) ? window.nullItems : [];
	var natures = Array.isArray(window.nullNatures) ? window.nullNatures : [];
	var locations = Array.isArray(window.nullLocations) ? window.nullLocations : [];
	var abilitiesBySpecies = (window.nullAbilities && typeof window.nullAbilities === "object") ? window.nullAbilities : {};

	function readBits(startBit, width) {
		var value = 0;
		for (var i = 0; i < width; i += 1) {
			var bitIndex = startBit + i;
			var byteIndex = bitIndex >> 3;
			if (byteIndex >= bytes.length) break;
			var bitOffset = bitIndex & 7;
			if (((bytes[byteIndex] >> bitOffset) & 1) !== 0) {
				value |= (1 << i);
			}
		}
		return value >>> 0;
	}

	function cleanSpeciesKey(speciesName) {
		return String(speciesName || "").toLowerCase().replace(/[^a-z0-9]/g, "");
	}

	function resolveAbilityName(speciesName, abilitySlot) {
		if (typeof nullResolveAbilityName === "function") {
			try {
				var direct = nullResolveAbilityName(speciesName, abilitySlot);
				if (direct) return direct;
			} catch (_err) {
			}
		}
		var speciesKey = cleanSpeciesKey(speciesName);
		var abilities = abilitiesBySpecies[speciesKey];
		if (!abilities || typeof abilities !== "object") {
			return "Unknown";
		}
		var preferredKeys = (abilitySlot === 0)
			? ["0", "1", "H"]
			: (abilitySlot === 1)
				? ["1", "0", "H"]
				: ["H", "0", "1"];
		for (var i = 0; i < preferredKeys.length; i += 1) {
			var ability = abilities[preferredKeys[i]];
			if (ability && ability !== "-" && ability !== "None") {
				return ability;
			}
		}
		return "Unknown";
	}

	var sections = [];
	for (var monIndex = 0; monIndex < monCount; monIndex += 1) {
		var bitPos = bitStart + (monIndex * MON_BITS);

		var speciesId = readBits(bitPos, 11); bitPos += 11;
		var level = readBits(bitPos, 7); bitPos += 7;
		var hpIV = readBits(bitPos, 5); bitPos += 5;
		var atkIV = readBits(bitPos, 5); bitPos += 5;
		var defIV = readBits(bitPos, 5); bitPos += 5;
		var spaIV = readBits(bitPos, 5); bitPos += 5;
		var spdIV = readBits(bitPos, 5); bitPos += 5;
		var speIV = readBits(bitPos, 5); bitPos += 5;
		var move1 = readBits(bitPos, 10); bitPos += 10;
		var move2 = readBits(bitPos, 10); bitPos += 10;
		var move3 = readBits(bitPos, 10); bitPos += 10;
		var move4 = readBits(bitPos, 10); bitPos += 10;
		var natureId = readBits(bitPos, 5); bitPos += 5;
		var itemId = readBits(bitPos, 10); bitPos += 10;
		var abilitySlot = readBits(bitPos, 2); bitPos += 2;
		var metLocationId = readBits(bitPos, 7); bitPos += 7;

		if (!speciesId) {
			continue;
		}

		if (level < 1) level = 1;
		if (level > 127) level = 127;

		var speciesName = (speciesId > 0 && speciesId <= mons.length && mons[speciesId - 1])
			? mons[speciesId - 1]
			: ("Species-" + String(speciesId));
		var natureName = (natureId >= 0 && natureId < natures.length && natures[natureId])
			? natures[natureId]
			: "Hardy";
		var itemName = (itemId > 0 && itemId <= items.length && items[itemId - 1])
			? items[itemId - 1]
			: "";
		var abilityName = resolveAbilityName(speciesName, abilitySlot);
		var metLocationName = (metLocationId >= 0 && metLocationId < locations.length && locations[metLocationId])
			? locations[metLocationId]
			: ("Unknown Location (0x" + metLocationId.toString(16).toUpperCase().padStart(2, "0") + ")");
		var moveIds = [move1, move2, move3, move4];
		var moveLines = [];
		for (var moveIdx = 0; moveIdx < moveIds.length; moveIdx += 1) {
			var moveId = moveIds[moveIdx];
			if (!moveId) continue;
			var moveName = (moveId >= 0 && moveId < moves.length && moves[moveId])
				? moves[moveId]
				: ("Move " + String(moveId));
			moveLines.push("- " + moveName);
		}

		var lead = speciesName;
		if (itemName) {
			lead += " @ " + itemName;
		}

			var monLines = [
				lead,
				"Ability: " + abilityName,
				"Level: " + String(level),
				natureName + " Nature",
				"IVs: " + String(hpIV) + " HP / " + String(atkIV) + " Atk / " + String(defIV) + " Def / " + String(spaIV) + " SpA / " + String(spdIV) + " SpD / " + String(speIV) + " Spe",
				"Met: " + metLocationName,
			];
		for (var lineIdx = 0; lineIdx < moveLines.length; lineIdx += 1) {
			monLines.push(moveLines[lineIdx]);
		}
		sections.push(monLines.join("\n"));
	}

	var tidLine = "";
	if (trainerId != null && secretId != null) {
		tidLine = "TID: " + String(trainerId) + ":" + String(secretId) + "\n";
	}
	if (sections.length <= 0) {
		return tidLine;
	}
	return tidLine + sections.join("\n\n") + "\n\n";
}

function decodeImperiumPackedBoxToShowdownText(payloadBytes) {
	var bytes = payloadBytes instanceof Uint8Array
		? payloadBytes
		: new Uint8Array(payloadBytes || new ArrayBuffer(0));
	if (bytes.length === 0) {
		return "";
	}

	var MON_BITS = 113;
	var bitStart = 0;
	var trainerId = null;
	var secretId = null;
	if (
		bytes.length >= 8 &&
		bytes[0] === 78 && // N
		bytes[1] === 66 && // B
		bytes[2] === 88 && // X
		bytes[3] === 49    // 1
	) {
		trainerId = (bytes[4] | (bytes[5] << 8)) >>> 0;
		secretId = (bytes[6] | (bytes[7] << 8)) >>> 0;
		bitStart = 64;
		try {
			if (typeof localStorage !== "undefined") {
				var combinedTrainerKey = (bytes[4] | (bytes[5] << 8) | (bytes[6] << 16) | (bytes[7] << 24)) >>> 0;
				localStorage.lastTid = String(combinedTrainerKey);
			}
		} catch (_err) {
		}
	}

	var totalBits = (bytes.length * 8) - bitStart;
	if (totalBits < 0) {
		totalBits = 0;
	}
	var monCount = Math.floor(totalBits / MON_BITS);

	var mons = (typeof emImpMons !== "undefined" && Array.isArray(emImpMons)) ? emImpMons : [];
	var moves = (typeof pokeemeraldMoves !== "undefined" && Array.isArray(pokeemeraldMoves)) ? pokeemeraldMoves : [];
	var items = (typeof emImpItems !== "undefined" && Array.isArray(emImpItems)) ? emImpItems : [];
	var natureNames = (typeof natures !== "undefined" && Array.isArray(natures)) ? natures : [];
	var emLocations = (typeof locations !== "undefined" && locations && Array.isArray(locations["EM"])) ? locations["EM"] : [];
	var primaryAbilitiesBySpecies = (typeof abilsPrimary === "object" && abilsPrimary) ? abilsPrimary : {};
	var fallbackAbilitiesBySpecies = (typeof abils === "object" && abils) ? abils : {};
	var primaryAbilityKeyLookup = {};
	var fallbackAbilityKeyLookup = {};
	for (var primaryAbilitySpecies in primaryAbilitiesBySpecies) {
		if (!Object.prototype.hasOwnProperty.call(primaryAbilitiesBySpecies, primaryAbilitySpecies)) {
			continue;
		}
		var primaryCleaned = String(primaryAbilitySpecies || "").toLowerCase().replace(/[^a-z0-9]/g, "");
		if (primaryCleaned && !primaryAbilityKeyLookup[primaryCleaned]) {
			primaryAbilityKeyLookup[primaryCleaned] = primaryAbilitiesBySpecies[primaryAbilitySpecies];
		}
	}
	for (var fallbackAbilitySpecies in fallbackAbilitiesBySpecies) {
		if (!Object.prototype.hasOwnProperty.call(fallbackAbilitiesBySpecies, fallbackAbilitySpecies)) {
			continue;
		}
		var fallbackCleaned = String(fallbackAbilitySpecies || "").toLowerCase().replace(/[^a-z0-9]/g, "");
		if (fallbackCleaned && !fallbackAbilityKeyLookup[fallbackCleaned]) {
			fallbackAbilityKeyLookup[fallbackCleaned] = fallbackAbilitiesBySpecies[fallbackAbilitySpecies];
		}
	}

	function readBits(startBit, width) {
		var value = 0;
		for (var i = 0; i < width; i += 1) {
			var bitIndex = startBit + i;
			var byteIndex = bitIndex >> 3;
			if (byteIndex >= bytes.length) break;
			var bitOffset = bitIndex & 7;
			if (((bytes[byteIndex] >> bitOffset) & 1) !== 0) {
				value |= (1 << i);
			}
		}
		return value >>> 0;
	}

	function formatItemName(itemName) {
		if (!itemName) {
			return "";
		}
		var raw = String(itemName);
		if (raw === "NONE") {
			return "";
		}
		if (typeof itemTitleize === "function") {
			try {
				var titleized = itemTitleize(raw);
				if (titleized) {
					return titleized;
				}
			} catch (_err) {
			}
		}
		return raw
			.toLowerCase()
			.split(/[_\s-]+/)
			.map(function (part) { return part ? (part.charAt(0).toUpperCase() + part.slice(1)) : ""; })
			.join(" ")
			.trim();
	}

	function resolveAbilityName(speciesName, abilitySlot) {
		var list = primaryAbilitiesBySpecies[speciesName];
		if (!Array.isArray(list)) {
			list = fallbackAbilitiesBySpecies[speciesName];
		}
		if (!Array.isArray(list)) {
			var speciesKey = String(speciesName || "").toLowerCase().replace(/[^a-z0-9]/g, "");
			list = primaryAbilityKeyLookup[speciesKey];
			if (!Array.isArray(list)) {
				list = fallbackAbilityKeyLookup[speciesKey];
			}
		}
		if (!Array.isArray(list)) {
			return "Unknown";
		}

		var preferredIndexes;
		if (abilitySlot === 0) {
			preferredIndexes = [0, 1, 2];
		} else if (abilitySlot === 1) {
			preferredIndexes = [1, 0, 2];
		} else {
			preferredIndexes = [2, 0, 1];
		}

		for (var i = 0; i < preferredIndexes.length; i += 1) {
			var abilityName = list[preferredIndexes[i]];
			if (abilityName && abilityName !== "None" && abilityName !== "-") {
				return abilityName;
			}
		}
		for (var j = 0; j < list.length; j += 1) {
			var fallbackAbility = list[j];
			if (fallbackAbility && fallbackAbility !== "None" && fallbackAbility !== "-") {
				return fallbackAbility;
			}
		}
		return "Unknown";
	}

	var sections = [];
	for (var monIndex = 0; monIndex < monCount; monIndex += 1) {
		var bitPos = bitStart + (monIndex * MON_BITS);

		var speciesId = readBits(bitPos, 11); bitPos += 11;
		var level = readBits(bitPos, 7); bitPos += 7;
		var hpIV = readBits(bitPos, 5); bitPos += 5;
		var atkIV = readBits(bitPos, 5); bitPos += 5;
		var defIV = readBits(bitPos, 5); bitPos += 5;
		var spaIV = readBits(bitPos, 5); bitPos += 5;
		var spdIV = readBits(bitPos, 5); bitPos += 5;
		var speIV = readBits(bitPos, 5); bitPos += 5;
		var move1 = readBits(bitPos, 10); bitPos += 10;
		var move2 = readBits(bitPos, 10); bitPos += 10;
		var move3 = readBits(bitPos, 10); bitPos += 10;
		var move4 = readBits(bitPos, 10); bitPos += 10;
		var natureId = readBits(bitPos, 5); bitPos += 5;
		var itemId = readBits(bitPos, 10); bitPos += 10;
		var abilitySlot = readBits(bitPos, 2); bitPos += 2;
		var metLocationId = readBits(bitPos, 8); bitPos += 8;

		if (!speciesId) {
			continue;
		}

		if (level < 1) level = 1;
		if (level > 127) level = 127;

		var speciesName = (speciesId >= 0 && speciesId < mons.length && mons[speciesId])
			? mons[speciesId]
			: ("Species-" + String(speciesId));
		if (typeof pokedex !== "undefined" && speciesName && speciesName !== "Species-" + String(speciesId) && !pokedex[speciesName]) {
			speciesName = speciesName.replaceAll(" ", "-");
		}

		var natureName = (natureId >= 0 && natureId < natureNames.length && natureNames[natureId])
			? natureNames[natureId]
			: "Hardy";
		var itemName = (itemId > 0 && itemId < items.length && items[itemId])
			? formatItemName(items[itemId])
			: "";
		var abilityName = resolveAbilityName(speciesName, abilitySlot);
		var metLocationName = (metLocationId >= 0 && metLocationId < emLocations.length && emLocations[metLocationId])
			? emLocations[metLocationId]
			: ("Unknown Location (0x" + metLocationId.toString(16).toUpperCase().padStart(2, "0") + ")");
		var moveIds = [move1, move2, move3, move4];
		var moveLines = [];
		for (var moveIdx = 0; moveIdx < moveIds.length; moveIdx += 1) {
			var moveId = moveIds[moveIdx];
			if (!moveId) continue;
			var moveName = (moveId >= 0 && moveId < moves.length && moves[moveId])
				? moves[moveId]
				: ("Move " + String(moveId));
			moveLines.push("- " + moveName);
		}

		var lead = speciesName;
		if (itemName) {
			lead += " @ " + itemName;
		}

		var monLines = [
			lead,
			"Ability: " + abilityName,
			"Level: " + String(level),
			natureName + " Nature",
			"IVs: " + String(hpIV) + " HP / " + String(atkIV) + " Atk / " + String(defIV) + " Def / " + String(spaIV) + " SpA / " + String(spdIV) + " SpD / " + String(speIV) + " Spe",
			"Met: " + metLocationName,
		];
		for (var lineIdx = 0; lineIdx < moveLines.length; lineIdx += 1) {
			monLines.push(moveLines[lineIdx]);
		}
		sections.push(monLines.join("\n"));
	}

	var tidLine = "";
	if (trainerId != null && secretId != null) {
		tidLine = "TID: " + String(trainerId) + ":" + String(secretId) + "\n";
	}
	if (sections.length <= 0) {
		return tidLine;
	}
	return tidLine + sections.join("\n\n") + "\n\n";
}

