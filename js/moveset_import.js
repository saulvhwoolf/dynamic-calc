function resolveTitleChangeMap(changeMap, title) {
	if (!changeMap) {
		return null
	}
	if (changeMap[title]) {
		return changeMap[title]
	}
	if (typeof title === "string" && title.includes("Platinum Redux")) {
		return changeMap["Platinum Redux"] || changeMap["Platinum Redux 2.6"] || null
	}
	return null
}

function getMoveChangesForTitle(title) {
	return resolveTitleChangeMap(moveChanges, title || TITLE) || {}
}

var pendingShowdownImportOptions = null;

function looksLikeSaveReaderShowdownText(showdownText) {
	var text = String(showdownText || "");
	return /^Met:\s*/mi.test(text) || /^Egg:\s*Yes\s*$/mi.test(text) || /^Ability Slot:\s*/mi.test(text);
}

function getShowdownImportOptions(showdownText, explicitOptions) {
	var options = {};
	if (pendingShowdownImportOptions && typeof pendingShowdownImportOptions === "object") {
		options = Object.assign(options, pendingShowdownImportOptions);
		pendingShowdownImportOptions = null;
	}
	if (explicitOptions && typeof explicitOptions === "object") {
		options = Object.assign(options, explicitOptions);
	}
	if (typeof options.applyRomReplacements === "undefined") {
		options.applyRomReplacements = looksLikeSaveReaderShowdownText(showdownText) || hasLoadedMoveReplacements();
	}
	return options;
}

function hasLoadedMoveReplacements() {
	if (typeof backup_data === "undefined" || !backup_data || !backup_data.move_replacements) {
		return false;
	}

	return Object.keys(backup_data.move_replacements).length > 0;
}

function shouldApplyImportRomReplacements(importOptions) {
	if (typeof importOptions === "undefined" || importOptions === null) {
		return true;
	}
	return !!importOptions.applyRomReplacements;
}

function getNamedReplacement(replacements, name) {
	if (!replacements || typeof replacements !== "object" || !name) {
		return null
	}

	if (Object.prototype.hasOwnProperty.call(replacements, name)) {
		return replacements[name]
	}

	var nameId = cleanString(name)
	if (Object.prototype.hasOwnProperty.call(replacements, nameId)) {
		return replacements[nameId]
	}

	for (var replacementKey in replacements) {
		if (cleanString(replacementKey) === nameId) {
			return replacements[replacementKey]
		}
	}

	return null
}

function canonicalizeImportedMoveReplacement(move) {
	if (!move) {
		return move
	}

	var moveName = String(move).trim()
	var resolvedMoveName = resolveImportedMoveNameFromDex(moveName)

	if (resolvedMoveName) {
		return resolvedMoveName
	}

	return moveName
}

function resolveImportedMoveNameFromDex(move) {
	if (!move) {
		return null
	}

	var moveName = String(move).trim()
	if (!moveName) {
		return null
	}

	var genMovesById = (typeof MOVES_BY_ID !== "undefined" && MOVES_BY_ID && MOVES_BY_ID[gen]) ? MOVES_BY_ID[gen] : null
	if (!genMovesById) {
		return moveName
	}

	if (genMovesById[moveName] && genMovesById[moveName].name) {
		return genMovesById[moveName].name
	}

	var moveId = cleanString(moveName)
	if (moveId && genMovesById[moveId] && genMovesById[moveId].name) {
		return genMovesById[moveId].name
	}

	return null
}

function normalizeImportedMoveName(move, importOptions) {
	if (shouldApplyImportRomReplacements(importOptions)) {
		var titleMoveChanges = getMoveChangesForTitle(TITLE)
		var titleReplacement = getNamedReplacement(titleMoveChanges, move)
		if (titleReplacement) {
			move = canonicalizeImportedMoveReplacement(titleReplacement)
		}

		if (backup_data["move_replacements"]) {
			let backupReplacement = getNamedReplacement(backup_data["move_replacements"], move)
			if (backupReplacement) {
				move = canonicalizeImportedMoveReplacement(backupReplacement)
			}
		}
	}

	if (!TITLE.includes("Sterling") && !TITLE.includes("Maximum") && !TITLE.includes("Ancestral")) {
		move = move.replace("HP ", "Hidden Power")
	}

	return resolveImportedMoveNameFromDex(move);
}




