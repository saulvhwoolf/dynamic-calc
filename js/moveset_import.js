var DDEX_TEMP_OPPONENT_MARKER = "### DDEX_TEMP_OPPONENT v1";
// slothsandmoons fork: opponent import now supports a whole team (array of injected
// temp sets) instead of a single mon. Kept the marker + set-shape so upstream stays MIT-compatible.
var ddexTemporaryOpponentSetRefs = [];

function isDdexTemporaryOpponentImportText(text) {
	return typeof text === "string" && text.indexOf(DDEX_TEMP_OPPONENT_MARKER) !== -1;
}

function getDdexTemporaryOpponentBlock(text) {
	var markerIndex = String(text || "").indexOf(DDEX_TEMP_OPPONENT_MARKER);
	if (markerIndex < 0) {
		return [];
	}

	var lines = String(text || "")
		.slice(markerIndex + DDEX_TEMP_OPPONENT_MARKER.length)
		.split(/\r?\n/);
	var block = [];
	var foundHeader = false;

	for (var i = 0; i < lines.length; i++) {
		var line = String(lines[i] || "").trim();
		if (line === DDEX_TEMP_OPPONENT_MARKER) {
			break;
		}
		if (!line) {
			if (foundHeader) break;
			continue;
		}

		foundHeader = true;
		block.push(line);
	}

	return block;
}

// Parse one Showdown block (array of trimmed, non-empty lines; block[0] is the header).
function parseDdexOpponentMon(block) {
	if (!block || !block.length) {
		return null;
	}

	var header = String(block[0] || "");
	var atParts = header.split("@");
	var namePart = atParts[0].trim();
	var item = atParts.length > 1 ? atParts.slice(1).join("@").trim() : "";

	var speciesName = findImportedSpeciesNameFromHeader(namePart);
	if (!speciesName || !pokedex[speciesName]) {
		return null;
	}

	var nickname = "";
	var pIdx = namePart.indexOf("(");
	if (pIdx > 0) {
		nickname = namePart.slice(0, pIdx).trim();
	}

	var parsed = {
		speciesName: speciesName,
		level: 100,
		ability: "",
		item: item,
		nickname: nickname,
		moves: [],
	};

	for (var i = 1; i < block.length; i++) {
		var line = block[i];
		var labelParts = line.split(":");
		var label = labelParts[0] ? labelParts[0].trim() : "";
		var value = labelParts.slice(1).join(":").trim();

		if (label === "Level") {
			var level = parseInt(value, 10);
			if (Number.isFinite(level) && level > 0) {
				parsed.level = level;
			}
			continue;
		}

		if (label === "Ability") {
			parsed.ability = value;
			if (abilityChanges[TITLE] && (abilityChanges[TITLE][cleanString(parsed.ability)] || abilityChanges[TITLE][parsed.ability])) {
				parsed.ability = abilityChanges[TITLE][parsed.ability] || ABILITIES_BY_ID[gen][abilityChanges[TITLE][cleanString(parsed.ability)]].name;
			}
			continue;
		}

		if (line[0] === "-" && parsed.moves.length < 4) {
			var move = line.substr(1).trim().replace("[", "").replace("]", "").replace("  ", " ");
			if (!move) continue;
			move = normalizeImportedMoveName(move, { applyRomReplacements: true });
			parsed.moves.push(move);
		}
	}

	return parsed;
}

// Parse a full opponent team from the marker onward. The marker line's trailing text is
// an optional trainer name; each blank-line-separated block after it is one mon.
function parseDdexTemporaryOpponentImport(text) {
	var markerIndex = String(text || "").indexOf(DDEX_TEMP_OPPONENT_MARKER);
	if (markerIndex < 0) {
		return null;
	}

	var lines = String(text || "")
		.slice(markerIndex + DDEX_TEMP_OPPONENT_MARKER.length)
		.split(/\r?\n/);
	var trainerName = (lines.shift() || "").trim();

	var blocks = [];
	var current = [];
	for (var i = 0; i < lines.length; i++) {
		var line = String(lines[i] || "").trim();
		if (line === DDEX_TEMP_OPPONENT_MARKER) {
			break;
		}
		if (!line) {
			if (current.length) {
				blocks.push(current);
				current = [];
			}
			continue;
		}
		current.push(line);
	}
	if (current.length) {
		blocks.push(current);
	}

	var mons = [];
	for (var b = 0; b < blocks.length; b++) {
		var mon = parseDdexOpponentMon(blocks[b]);
		if (mon) {
			mons.push(mon);
		}
	}

	if (!mons.length) {
		return null;
	}

	return { trainerName: trainerName || "Opponent", mons: mons };
}

function removeDdexTemporaryOpponentSet() {
	if (!Array.isArray(ddexTemporaryOpponentSetRefs) || !setdex) {
		ddexTemporaryOpponentSetRefs = [];
		return;
	}

	for (var i = 0; i < ddexTemporaryOpponentSetRefs.length; i++) {
		var ref = ddexTemporaryOpponentSetRefs[i];
		if (!ref) continue;
		var speciesName = ref.speciesName;
		var setName = ref.setName;
		if (setdex[speciesName] && setdex[speciesName][setName] && setdex[speciesName][setName].isDdexTeamMon) {
			delete setdex[speciesName][setName];
			if (Object.keys(setdex[speciesName]).length === 0) {
				delete setdex[speciesName];
			}
		}
	}

	ddexTemporaryOpponentSetRefs = [];
	if (typeof get_trainer_names === "function") {
		try { TR_NAMES = get_trainer_names(); } catch (e) {}
	}
}

function buildDdexTemporaryOpponentSet(parsed, meta) {
	var zeroEvs = { hp: 0, at: 0, df: 0, sa: 0, sd: 0, sp: 0, sl: 0 };
	var perfectIvs = { hp: 31, at: 31, df: 31, sa: 31, sd: 31, sp: 31, sl: 31 };
	meta = meta || {};

	return {
		level: parsed.level,
		ability: parsed.ability || (pokedex[parsed.speciesName].abilities && pokedex[parsed.speciesName].abilities[0]) || "",
		moves: parsed.moves.slice(0, 4),
		nature: "Hardy",
		item: parsed.item || "",
		evs: zeroEvs,
		ivs: perfectIvs,
		status: "Healthy",
		nickname: parsed.nickname || "",
		sub_index: typeof meta.sub_index === "number" ? meta.sub_index : 0,
		tr_id: meta.tr_id || "",
		isCustomSet: true,
		// Marks our injected opponent-team sets. NOT isTemporaryOpponentSet — that flag routes
		// selection into the lone-wild branch (no party); we want these to group as a trainer.
		isDdexTeamMon: true,
	};
}

