function getStats(currentPoke, rows, offset, importOptions) {
	currentPoke.nature = "Serious";
	currentPoke.status = "Healthy";
	currentPoke.isEgg = false;
	var currentEV;
	var currentIV;
	var currentAbility;
	var currentNature;
	var natureIsSet = false
	currentPoke.level = 100;
	for (var x = offset; x < offset + 8; x++) {
		if (isImportedSetBoundaryLine(rows, x, offset, importOptions)) {
			break;
		}
		var currentRow = rows[x] ? rows[x].split(/[/:]/) : '';
		var evs = {};
		var ivs = {};
		var ev;
		var j;

		switch (currentRow[0]) {
		case 'Level':
			var levelValue = currentRow[1] ? currentRow[1].trim() : "";
			var expMatch = levelValue.match(/^(\d+)\s+EXP$/);
			if (expMatch) {
				var expValue = parseInt(expMatch[1], 10);
				var resolvedLevel = resolveSavLevelFromExperience(currentPoke.name, expValue);
				if (Number.isFinite(resolvedLevel)) {
					currentPoke.level = resolvedLevel;
				} else {
					console.warn("Failed to resolve EXP-based level for species:", currentPoke.name, "EXP:", expValue);
					currentPoke.level = 1;
				}
				break;
			}
			currentPoke.level = parseInt(levelValue);
			break;
		case 'Ability Slot':
			currentPoke.abilitySlotId = parseInt(currentRow[1]);
			break;
		case 'Egg':
			currentPoke.isEgg = String(currentRow[1] || "").trim().toLowerCase() === "yes";
			break;
		case 'EVs':
			for (j = 1; j < currentRow.length; j++) {
				currentEV = currentRow[j].trim().split(" ");
				currentEV[1] = statToLegacyStat(currentEV[1].toLowerCase());
				evs[currentEV[1]] = parseInt(currentEV[0]);
			}
			currentPoke.evs = evs;
			break;
		case 'IVs':
			for (j = 1; j < currentRow.length; j++) {
				currentIV = currentRow[j].trim().split(" ");
				currentIV[1] = statToLegacyStat(currentIV[1].toLowerCase());
				ivs[currentIV[1]] = parseInt(currentIV[0]);
			}
			currentPoke.ivs = ivs;
			break;
		case 'Status':
			currentPoke.status = typeof normalizeStoredStatus === "function" ? normalizeStoredStatus(currentRow[1]) : "Healthy";
			break;

		}
		currentAbility = rows[x] ? rows[x].trim().split(":") : '';
		if (currentAbility[0] == "Ability") {
			currentPoke.ability = currentAbility[1].trim();
			
			if (abilityChanges[TITLE] && (abilityChanges[TITLE][cleanString(currentPoke.ability)] || abilityChanges[TITLE][currentPoke.ability])) {
				currentPoke.ability = abilityChanges[TITLE][currentPoke.ability] || ABILITIES_BY_ID[gen][abilityChanges[TITLE][cleanString(currentPoke.ability)]].name
			}
		}

		currentNature = rows[x] ? rows[x].trim().split(" ") : '';
		if (currentNature[1] == "Nature" && !natureIsSet) {
			currentPoke.nature = currentNature[0];
			natureIsSet = true
		}
	}
	return currentPoke;
}

