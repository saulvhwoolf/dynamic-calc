function getImportedSpeciesMatchesFromHeader(headerLine, importOptions) {
	var headerInfo = extractGenderFromImportHeader(headerLine);
	var currentRow = headerInfo.header.split(/[()@]/);
	var speciesParts = headerInfo.header.split("@")[0].split(/[()]/);
	var matches = [];

	for (var i = 0; i < speciesParts.length; i++) {
		var rawCandidate = speciesParts[i].trim();
		if (!rawCandidate) {
			continue;
		}
		if (i > 0 && /^[MFN]$/i.test(rawCandidate)) {
			continue;
		}
		var candidate = checkExeptions(rawCandidate, importOptions);
		candidate = resolveRadicalRedGenderedSpeciesName(candidate, headerInfo.gender, importOptions);
		if (calc.SPECIES[8][candidate] !== undefined) {
			matches.push({
				index: i,
				speciesName: candidate,
				rawName: rawCandidate
			});
		}
	}

	return {
		headerInfo: headerInfo,
		parts: currentRow,
		matches: matches
	};
}

function findImportedSpeciesMatchFromHeader(headerLine, importOptions) {
	var parsedHeader = getImportedSpeciesMatchesFromHeader(headerLine, importOptions);
	var eggCandidate = null;
	var firstNonEggCandidate = null;
	var lastParenthesizedNonEggCandidate = null;

	for (var i = 0; i < parsedHeader.matches.length; i++) {
		var match = parsedHeader.matches[i];
		if (match.speciesName === "Egg" || match.speciesName === "Bad Egg") {
			if (!eggCandidate) {
				eggCandidate = match;
			}
			continue;
		}
		if (!firstNonEggCandidate) {
			firstNonEggCandidate = match;
		}
		if (match.index > 0) {
			lastParenthesizedNonEggCandidate = match;
		}
	}

	return {
		headerInfo: parsedHeader.headerInfo,
		parts: parsedHeader.parts,
		match: lastParenthesizedNonEggCandidate || firstNonEggCandidate || eggCandidate
	};
}

function findImportedSpeciesNameFromHeader(headerLine, importOptions) {
	var speciesMatch = findImportedSpeciesMatchFromHeader(headerLine, importOptions);
	return speciesMatch.match ? speciesMatch.match.speciesName : null;
}

function isImportedSetBoundaryLine(rows, rowIndex, firstDataRowIndex, importOptions) {
	if (!Array.isArray(rows) || rowIndex <= firstDataRowIndex || !rows[rowIndex]) {
		return false;
	}

	var trimmed = String(rows[rowIndex] || "").trim();
	if (!trimmed) {
		return false;
	}

	if (
		trimmed[0] == "-" ||
		trimmed.indexOf("Level:") === 0 ||
		trimmed.indexOf("Ability:") === 0 ||
		trimmed.indexOf("Ability Slot:") === 0 ||
		trimmed.indexOf("EVs:") === 0 ||
		trimmed.indexOf("IVs:") === 0 ||
		trimmed.indexOf("Status:") === 0 ||
		trimmed.indexOf("Met:") === 0 ||
		trimmed.indexOf("TID:") === 0 ||
		trimmed.indexOf("Egg:") === 0 ||
		/\sNature$/i.test(trimmed)
	) {
		return false;
	}

	return Boolean(findImportedSpeciesNameFromHeader(trimmed, importOptions));
}

