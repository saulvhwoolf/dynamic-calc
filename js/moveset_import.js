// Inject the whole opponent team into setdex so every mon is selectable on the right
// (getSetOptions reads setdex live); auto-select the ace/lead. All injected sets are
// tracked for cleanup when a non-temporary opponent is later chosen.
function importDdexTemporaryOpponent(text) {
	var team = parseDdexTemporaryOpponentImport(text);
	if (!team || !team.mons.length) {
		alert("No temporary ddex opponent imported, please check your syntax and try again");
		return false;
	}

	removeDdexTemporaryOpponentSet();

	var tr_id = "ddex_temp_" + String(team.trainerName).replace(/[^a-z0-9]/gi, "").toLowerCase();
	var refs = [];
	var firstSpecies = null;
	var firstSetName = null;

	for (var i = 0; i < team.mons.length; i++) {
		var mon = team.mons[i];
		// Trailing space is load-bearing: get_trainer_poks groups a trainer's mons by
		// finding "Lvl <n> <trainer> " (with the space) in each set id, matching the real
		// data's naming. Without it the mons don't group into a selectable party.
		var setName = "Lvl " + mon.level + " " + team.trainerName + " ";
		if (setdex[mon.speciesName] && setdex[mon.speciesName][setName]) {
			setName = "Lvl " + mon.level + " " + team.trainerName + " " + (i + 1) + " ";
		}
		if (!setdex[mon.speciesName]) {
			setdex[mon.speciesName] = {};
		}
		setdex[mon.speciesName][setName] = buildDdexTemporaryOpponentSet(mon, { sub_index: i, tr_id: tr_id });
		refs.push({ speciesName: mon.speciesName, setName: setName });
		if (i === 0) {
			firstSpecies = mon.speciesName;
			firstSetName = setName;
		}
	}

	ddexTemporaryOpponentSetRefs = refs;
	if (typeof get_trainer_names === "function") {
		try { TR_NAMES = get_trainer_names(); } catch (e) {}
	}

	selectDdexTemporaryOpponent(firstSpecies, firstSetName);
	return true;
}

function looksLikeImportableShowdownText(text) {
	if (typeof text !== "string" || !text.trim() || isValidJSON(text)) {
		return false;
	}

	var rows = text.split("\n");
	var foundHeader = false;
	var foundBody = false;

	for (var i = 0; i < rows.length; i++) {
		var trimmed = rows[i].trim();
		if (!trimmed) {
			continue;
		}

		if (!foundHeader && findImportedSpeciesNameFromHeader(trimmed)) {
			foundHeader = true;
		}
		if (!foundBody && (trimmed.indexOf("Level:") === 0 || trimmed.indexOf("- ") === 0)) {
			foundBody = true;
		}
		if (foundHeader && foundBody) {
			return true;
		}
	}

	return false;
}

